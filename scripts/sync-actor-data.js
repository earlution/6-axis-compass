const fs = require('fs');
const path = require('path');

const DATA_DIR = path.join(__dirname, '..', 'data', 'actors');
const OUT_FILE = path.join(__dirname, '..', 'src', 'actors-generated.js');

function loadActors() {
  const files = fs.readdirSync(DATA_DIR).filter(f => f.endsWith('.json'));
  const actors = [];

  for (const file of files.sort()) {
    const raw = JSON.parse(fs.readFileSync(path.join(DATA_DIR, file), 'utf-8'));

    // Transform nested JSON into flat format compatible with existing code,
    // plus _actorMeta and _scoreMeta for traceability UI
    const scores = {};
    const scoreMeta = {};

    for (const [axis, data] of Object.entries(raw.scores)) {
      scores[axis] = data.value;
      scoreMeta[axis] = {
        confidence: data.confidence,
        rationale: data.rationale,
        sources: data.sources || []
      };
    }

    const actor = {
      name: raw.actor.name,
      scores,
      color: raw.actor.color,
      _actorMeta: {
        slug: raw.actor.slug,
        category: raw.actor.category,
        activePeriod: raw.actor.activePeriod,
        version: raw.actor.version,
        lastUpdated: raw.actor.lastUpdated,
        curator: raw.actor.curator,
        contributors: raw.actor.contributors
      },
      _scoreMeta: scoreMeta
    };

    if (raw.responses) {
      actor.responses = {};
      for (const [qid, data] of Object.entries(raw.responses)) {
        actor.responses[qid] = data.value;
      }
    }

    actors.push(actor);
  }

  return actors;
}

const actors = loadActors();

const output = `// AUTO-GENERATED by scripts/sync-actor-data.js
// Do not edit directly. Edit files in data/actors/ instead.
const __ACTORS = ${JSON.stringify(actors, null, 2)};
`;

fs.writeFileSync(OUT_FILE, output, 'utf-8');
console.log(`Generated src/actors-generated.js with ${actors.length} actors`);
